from datetime import UTC, datetime  # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º UTC –∏ datetime –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –≤—Ä–µ–º–µ–Ω–µ–º
